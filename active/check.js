async function checkServiceStatus() {
  const url = 'https://axess.rhw.one/ping';
  const payload = { name: 'UVv2' };

  try {
      const response = await fetch(url, {
          method: 'POST',
          headers: {
              'Content-Type': 'application/json'
          },
          body: JSON.stringify(payload)
      });

      console.log(`Service check performed. Status: ${response.status}`);

      
      if (response.status === 201) {
          const body = document.body;
          body.classList.add('deactivated'); 

          const deactivatedContent = `
              <h1>Service Deactivated By</h1>
              <div class="svg-container">
                  <img src="https://cdn.jsdelivr.net/gh/rhenryw/rhw.one-git@main/cdn/axess.png" alt="AXESS Logo">
              </div>
          `;
          body.innerHTML = deactivatedContent; 
          clearInterval(checkInterval);
      } else if (response.status !== 200) {
          console.warn(`Unexpected status code: ${response.status}`);
      }

  } catch (error) {
      console.error('Error checking service status:', error);
  }
}


document.addEventListener('DOMContentLoaded', () => {
  checkServiceStatus();
});

checkServiceStatus()

const checkInterval = setInterval(checkServiceStatus, 120000);

